// Creating Arrays in JS

// Using Array Literals
const nums = [13, 15, 45, 84, 19];
console.log(nums);

// Using Array Constructors

const fruits = new Array("Apple", "Oranges", "Bananas", "Grapes");
console.log(fruits);

// Accessing Array Elements

console.log(fruits[0], fruits[2]);

// Adding Element At An Index
fruits[3] = "Pineapple";

// Adding an element at any random index
fruits[5] = "Litchi";
console.log(fruits);
